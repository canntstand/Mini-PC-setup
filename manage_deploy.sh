#!/bin/bash

set -e

GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

show_header() {
    clear
    echo -e "${BLUE}======================================================================${NC}"
    echo -e "${BLUE}          🚀 Интерактивный мастер деплоя: ServeHub-2${NC}"
    echo -e "${BLUE}======================================================================${NC}"
    echo -e "Выберите сценарий развертывания инфраструктуры:"
    echo ""
}

show_header

echo -e "1) ${GREEN}Полная установка с нуля (VPS + Локальный)${NC}"
echo -e "   • Сначала настроит чистую VPS по паролю root."
echo -e "   • Затем сделает паузу для WireGuard на localhost."
echo -e "   • В конце развернет сервисы на локальном сервере по SSH-ключу."
echo -e "----------------------------------------------------------------------"
echo -e "2) ${YELLOW}Только удаленный сервер / VPS с нуля${NC}"
echo -e "   • Настроит ОС и запустит контейнеры только на VPS."
echo -e "   • Потребуется пароль root. Шаг локального сервера полностью пропускается."
echo -e "----------------------------------------------------------------------"
echo -e "3) ${YELLOW}Только локальный сервер (Быстрый деплой приложений)${NC}"
echo -e "   • Перезапустит/обновит сервисы только на локальном сервере по SSH-ключу."
echo -e "   • Шаг ожидания WireGuard и подготовка VPS пропускаются."
echo -e "----------------------------------------------------------------------"
echo -e "4) ${YELLOW}Только локальный сервер + Ожидание WireGuard${NC}"
echo -e "   • Полезно, если нужно перегенерировать туннель AmneziaWG."
echo -e "   • Сначала покажет паузу с инструкцией, а затем настроит локальный сервер."
echo -e "----------------------------------------------------------------------"
echo -e "5) ${RED}Выход из мастера деплоя${NC}"
echo ""

read -p "Введите номер варианта [1-5]: " CHOICE

case $CHOICE in
    1)
        echo -e "\n${BLUE}>>> [1/2] Запуск первичной подготовки и настройки VPS (нужен пароль root)...${NC}"
        ansible-playbook ansible/deploy.yml --limit vps -k
        
        echo -e "\n${BLUE}>>> [2/2] Запуск шага WireGuard и настройки локального сервера...${NC}"
        ansible-playbook ansible/deploy.yml --limit "local,localhost" --skip-tags bootstrap
        ;;
    2)
        echo -e "\n${BLUE}>>> Запуск полной настройки VPS с нуля (нужен пароль root)...${NC}"
        ansible-playbook ansible/deploy.yml --limit vps -k
        ;;
    3)
        echo -e "\n${BLUE}>>> Накатывание сервисов на локальный сервер (по SSH-ключу, без пауз)...${NC}"
        ansible-playbook ansible/deploy.yml --limit local --skip-tags bootstrap
        ;;
    4)
        echo -e "\n${BLUE}>>> Накатывание сервисов на локальный сервер + Пауза WireGuard...${NC}"
        ansible-playbook ansible/deploy.yml --limit "local,localhost" --skip-tags bootstrap
        ;;
    5)
        echo -e "\n${RED}Выход. Никакие команды не были выполнены.${NC}"
        exit 0
        ;;
    *)
        echo -e "\n${RED}❌ Неверный ввод! Пожалуйста, запустите скрипт заново и выберите число от 1 до 5.${NC}"
        exit 1
        ;;
esac

echo ""
echo -e "${GREEN}✔ Операция успешно завершена! Инфраструктура обновлена.${NC}"